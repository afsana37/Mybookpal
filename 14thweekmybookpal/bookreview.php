<h2>The power of Habit</h2>
<p>In The Power of Habit, Pulitzer Prize–winning business reporter Charles Duhigg takes us to the thrilling edge of scientific discoveries that explain why habits exist and how they can be changed. Distilling vast amounts of information into engrossing narratives that take us from the boardrooms of Procter & Gamble to sidelines of the NFL to the front lines of the civil rights movement, Duhigg presents a whole new understanding of human nature and its potential. At its core, The Power of Habit contains an exhilarating argument: The key to exercising regularly, losing weight, being more productive, and achieving success is understanding how habits work. As Duhigg shows, by harnessing this new science, we can transform our businesses, our communities, and our lives.</p>

<p>The Power of Habit recounts a number of useful illustrations of the role of habits in individuals, organizations, and societies, and follows up with practical techniques to recognize and consciously direct the things that really control our behavior and our results. The power of habit is the ability to automate willpower, turning a painfully scarce resource into an infinite one. Put this book’s subject matter into practice, and the change in your results will be profound.</p>


