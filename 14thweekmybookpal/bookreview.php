<h2>Sherlock holmes</h2>
<p>The Adventures of Sherlock Holmes is a compilation of twelve short Sherlock Holmes stories that was published on October 31st, 1892 and written by Sir Arthur Conan Doyle. The character, Sherlock Holmes, was previously introduced to the world in a novel called A Study in Scarlet, in a Christmas 'annual.'</p>

 <p>Sherlock Holmes is probably the most well-known and loved fictional detective in literature. It's interesting to note that Sir Arthur Conan Doyle actually modeled his famous detective after an associate, Dr. Joseph Bell. Dr. Bell had a long, thin nose - much as we picture Sherlock Holmes - and was very observant. He also used strong deductive skills. </p>
